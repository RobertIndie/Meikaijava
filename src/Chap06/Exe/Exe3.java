package Chap06.Exe;

public class Exe3 {
    public static void main(String[] args) {
        Double[] a = new Double[5];//数组声明
        a[0] = 1.1;
        a[1] = 2.2;
        a[2] = 3.3;
        a[3] = 4.4;
        a[4] = 5.5;

        //显示全部元素的值
        System.out.println("a[" + 0 + "]=" + a[0]);
        System.out.println("a[" + 1 + "]=" + a[1]);
        System.out.println("a[" + 2 + "]=" + a[2]);
        System.out.println("a[" + 3 + "]=" + a[3]);
        System.out.println("a[" + 4 + "]=" + a[4]);
    }
}
